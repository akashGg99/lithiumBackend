
    //trimming function
function trimmingFunction(str) {
    return str.trim();
}

function changetoLowercase(str2) {
   return str2.toLowerCase();
}

function changetoUppercase(str3) {
    return str3.toUpperCase();
}





module.exports.trimmingFunction = trimmingFunction;
module.exports.changetoLowercase = changetoLowercase;
module.exports.changetoUppercase = changetoUppercase;
